DROP DATABASE aeroporto;

CREATE DATABASE aeroporto;

USE aeroporto;

CREATE TABLE aeroporto (
	
	cod_aeroporto 	BIGINT,
	nome 			CHARACTER VARYING(100),
	cidade 			CHARACTER VARYING(100),
	pais 			CHARACTER VARYING(100),
	
	PRIMARY KEY (cod_aeroporto)
);

CREATE TABLE companhia (

	cod_companhia 	BIGINT,
	nome 			CHARACTER VARYING(100),
	pais 			CHARACTER VARYING(100),
	
	PRIMARY KEY (cod_companhia)
);

CREATE TABLE piloto (

	cod_piloto 		BIGINT,
	cod_companhia 	BIGINT,
	nome 			CHARACTER VARYING(100),
	salario 		CHARACTER VARYING(100),
	gratificacao 	CHARACTER VARYING(100),
	
	PRIMARY KEY (cod_piloto),
	FOREIGN KEY (cod_companhia) REFERENCES companhia(cod_companhia)
);

CREATE TABLE voo (

	cod_voo 				CHARACTER VARYING(5),
	cod_aeroporto_origem 	BIGINT,
	cod_aeroporto_destino 	BIGINT,
	cod_companhia 			BIGINT,
	
	PRIMARY KEY (cod_voo),
	FOREIGN KEY (cod_aeroporto_origem) REFERENCES aeroporto(cod_aeroporto),
	FOREIGN KEY (cod_aeroporto_destino) REFERENCES aeroporto(cod_aeroporto),
	FOREIGN KEY (cod_companhia) REFERENCES companhia(cod_companhia)
);

CREATE TABLE partida (

	cod_partida		BIGINT,
	cod_voo 		CHARACTER VARYING(5),
	cod_piloto 		BIGINT,
	data 			DATE,
	hora 			TIME,
	aviao			CHARACTER VARYING(100),
	
	PRIMARY KEY (cod_partida),
	FOREIGN KEY (cod_voo) REFERENCES voo(cod_voo),
	FOREIGN KEY (cod_piloto) REFERENCES piloto(cod_piloto)
);

